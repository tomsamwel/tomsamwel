// mutations
const mutations = {
    setParticipants(state, participants) {
        state.participants = participants;
    },

    updateParticipant(state, updatedParticipant) {
        let participant = state.participants.find(
            (participant) => participant.id === updatedParticipant.id
        );
        let index = state.participants.indexOf(participant);

        Object.assign(participant, updatedParticipant);

        Vue.set(state.participants, index, participant);
    },
};

